function init() {
    var frame = document.getElementById("videoData");
    frame.width = window.innerWidth * 0.4;
    frame.height = window.innerHeight * 0.9;
}
// ============================================================================================================
function initGoogle() {
    var frame = document.getElementById("videoData");
    frame.src = "https://www.bing.com";
}